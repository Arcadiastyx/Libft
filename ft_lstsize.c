#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	unsigned int	size;

	size = 0;
	while (lst)
	{
		lst = lst->next;
		size++;
	}
	return (size);
}
/*
lst: Le début de la liste.
retour valeur : Taille de la liste
Compte le nombre d’éléments de la liste.
*/
